# Define dictionary
user_0 = {
    'username': 'efermi',
    'first': 'enrico',
    'last': 'fermi',
    }

# Create for loop for key-value pairs
for key, value in user_0.items():
    print(f"\nKey: {key}")
    print(f"Value: {value}")

